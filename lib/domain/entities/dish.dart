class Dish {
  final int id;
  final String title;
  final int weight;
  final String ingredients;
  final int calories;
  final double price;
  final String imageUrl;

  Dish({
    required this.id,
    required this.title,
    required this.weight,
    required this.ingredients,
    required this.calories,
    required this.price,
    required this.imageUrl,
  });
}